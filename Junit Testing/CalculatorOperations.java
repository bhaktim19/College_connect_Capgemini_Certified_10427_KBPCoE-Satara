public class CalculatorOperations {
	
	public int addition() {
		int a,b,sum;
		a=10;
		b=20;
		sum=a+b;
		return sum;
	}
	
	public int subtraction() {
		int a,b;
		a=20;
		b=10;
		int s=a-b;
		return s;
	}
	
	public int multiplication() {
		int a,b;
		a=2;
		b=4;
		int m=a*b;
		return m;
	}
	
	public int division() {
		int a,b;
		a=10;
		b=5;
		int d=a/b;
		return d;
	}

}
